# Exercise 2 : Conversion between types
a = 12
# Convert a into float
a = float(a)
print(a)

pi = 3.141592654
# Convert pi into int
pi = int(pi)
print(pi)

number_str = "6174.6174"
# Convert number_str into int
number_str = int(float(number_str))
print(number_str)

true_str = "True"
# Convert true_str into boolean
true_str == "True"
print(true_str)

none = None
# Convert none into str
none = str(none)
print(none)

false = False
# Convert false into str
false = str(false)
print(false)
